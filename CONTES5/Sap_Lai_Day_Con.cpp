#include<bits/stdc++.h>
using namespace std;
main(){
    int t;
    cin>>t;
    while(t--){
        long n;
        cin>>n;
        long a[n],b[n];
        for(int i=0;i<n;i++){
            cin>>a[i];
            b[i]=a[i];
        }
        sort(a,a+n);
        long i=0,j=n-1;
        while(a[i]==b[i]&&i<n) i++;
        while(a[j]==b[j]&&j>=0) j--;
        cout<<i+1<<" "<<j+1<<endl;
        
    }
}
